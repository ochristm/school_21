#include <unistd.h>

int	is_sep(int i)
{
	if (i == '\f' || i == '\v' || i == '\n' || i == '\t' || i == ' ')
		return (1);
	return (0);
}

int	main(int ac, char *av[])
{
	int i;

	i = 0;
	if (ac == 2)
	{
		while (is_sep(av[1][i]) == 1)
			i++;
		while (is_sep(av[1][i]) == 0 && av[1][i])
			write(1, &av[1][i++], 1);
	}
	write(1, "\n", 1);
	return (0);
}
