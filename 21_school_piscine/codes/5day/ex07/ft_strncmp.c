int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	const char	*end;
	int			res;

	end = s1 + n;
	res = 0;
	while (res == 0 && s1 != end && (*s1 || *s2))
	{
		res = *(s1++) - *(s2++);
	}
	return (res);
}
